//Add
function add(n1, n2) {
   return n1 + n2 
}

//Subtract
function subtract(n1, n2) {
   return n1 - n2    
}


//Multiply
function multiply(n1, n2) {
   return n1 * n2
}


//Divide
function divide(n1, n2) {
    return n1 / n2
}


console.log(add(16, 10), subtract(50, 25), multiply(12, 12), divide(40, 10))